/**
 * EpitopX AI — Générateur de structures PDB
 * Génère des coordonnées PDB réalistes (modèle hélice alpha) à partir de séquences protéiques.
 * Utilisé pour la visualisation 3D locale.
 */

const PDBGenerator = (() => {
  const AA_MAP = {
    'A': 'ALA', 'R': 'ARG', 'N': 'ASN', 'D': 'ASP', 'C': 'CYS',
    'E': 'GLU', 'Q': 'GLN', 'G': 'GLY', 'H': 'HIS', 'I': 'ILE',
    'L': 'LEU', 'K': 'LYS', 'M': 'MET', 'F': 'PHE', 'P': 'PRO',
    'S': 'SER', 'T': 'THR', 'W': 'TRP', 'Y': 'TYR', 'V': 'VAL'
  };

  // Alpha helix parameters
  const HELIX_RISE = 1.5;       // Rise per residue (Å)
  const HELIX_TURN = 100;       // Degrees per residue
  const HELIX_RADIUS = 2.3;     // Helix radius (Å)

  /**
   * Generate a PDB format string from a protein sequence.
   * Models an alpha helix for visualization.
   */
  function generate(sequence, title = 'Generated') {
    if (!sequence) return '';

    const lines = [];
    lines.push(formatPDBLine('HEADER', `EPITOPX AI GENERATED MODEL - ${title}`));
    lines.push(formatPDBLine('TITLE', `ALPHA HELIX MODEL OF ${title.toUpperCase()}`));
    lines.push(formatPDBLine('REMARK', '1 GENERATED BY EPITOPX AI PDB GENERATOR'));
    lines.push(formatPDBLine('REMARK', '2 THIS IS A SIMULATED STRUCTURE FOR VISUALIZATION'));

    let atomSerial = 1;

    for (let i = 0; i < sequence.length; i++) {
      const aa = sequence[i];
      const resName = AA_MAP[aa] || 'UNK';
      const resSeq = i + 1;
      const theta = (HELIX_TURN * i * Math.PI) / 180;

      // CA position (on the helix)
      const ca_x = HELIX_RADIUS * Math.cos(theta);
      const ca_y = HELIX_RADIUS * Math.sin(theta);
      const ca_z = HELIX_RISE * i;

      // B-factor varies along the chain (simulated flexibility)
      const bfactor = 15 + 10 * Math.sin(i * 0.5) + Math.random() * 5;

      // N atom (offset from CA)
      const n_x = ca_x + 0.47 * Math.cos(theta + 0.35);
      const n_y = ca_y + 0.47 * Math.sin(theta + 0.35);
      const n_z = ca_z - 0.65;
      lines.push(formatATOM(atomSerial++, 'N', resName, 'A', resSeq, n_x, n_y, n_z, 1.0, bfactor, 'N'));

      // CA atom
      lines.push(formatATOM(atomSerial++, 'CA', resName, 'A', resSeq, ca_x, ca_y, ca_z, 1.0, bfactor, 'C'));

      // C atom (offset from CA)
      const c_x = ca_x + 0.52 * Math.cos(theta - 0.35);
      const c_y = ca_y + 0.52 * Math.sin(theta - 0.35);
      const c_z = ca_z + 0.65;
      lines.push(formatATOM(atomSerial++, 'C', resName, 'A', resSeq, c_x, c_y, c_z, 1.0, bfactor, 'C'));

      // O atom (offset from C)
      const o_x = c_x + 0.85 * Math.cos(theta - 0.7);
      const o_y = c_y + 0.85 * Math.sin(theta - 0.7);
      const o_z = c_z + 0.25;
      lines.push(formatATOM(atomSerial++, 'O', resName, 'A', resSeq, o_x, o_y, o_z, 1.0, bfactor, 'O'));

      // CB atom (not for Glycine)
      if (aa !== 'G') {
        const cb_x = ca_x + 1.2 * Math.cos(theta + Math.PI * 0.6);
        const cb_y = ca_y + 1.2 * Math.sin(theta + Math.PI * 0.6);
        const cb_z = ca_z + 0.3;
        lines.push(formatATOM(atomSerial++, 'CB', resName, 'A', resSeq, cb_x, cb_y, cb_z, 1.0, bfactor, 'C'));
      }
    }

    lines.push('TER');
    lines.push('END');

    return lines.join('\n');
  }

  function formatATOM(serial, name, resName, chain, resSeq, x, y, z, occupancy, bfactor, element) {
    const atomName = name.length < 4 ? ` ${name}` : name;
    return 'ATOM  '
      + String(serial).padStart(5)
      + ' '
      + atomName.padEnd(4)
      + ' '
      + resName.padEnd(3)
      + ' '
      + chain
      + String(resSeq).padStart(4)
      + '    '
      + x.toFixed(3).padStart(8)
      + y.toFixed(3).padStart(8)
      + z.toFixed(3).padStart(8)
      + occupancy.toFixed(2).padStart(6)
      + bfactor.toFixed(2).padStart(6)
      + '          '
      + element.padStart(2);
  }

  function formatPDBLine(record, text) {
    return (record + '  ').substring(0, 6) + text;
  }

  return { generate };
})();
