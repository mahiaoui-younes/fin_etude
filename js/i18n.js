// Memoized active-language dictionary — rebuilt only when the language actually
// changes. All 8 dictionaries remain in memory (no bundler / lazy-loading in
// this MPA setup) but t() and applyTranslations() now resolve the dict in O(1)
// instead of traversing the full translations object on every call.
let _cachedLang = null;
let _cachedDict = null;

function _getDict() {
  const lang = getCurrentLang();
  if (lang !== _cachedLang) {
    _cachedLang = lang;
    _cachedDict = translations[lang] || translations['fr'];
  }
  return _cachedDict;
}

/**
 * Translate a key for the current language.
 * Falls back to the key itself if not found.
 */
function t(key) {
  const dict = _getDict();
  return (dict && dict[key]) || (translations['en'] && translations['en'][key]) || key;
}

function getCurrentLang() {
  return localStorage.getItem('lang') || 'fr';
}

/**
 * Apply all data-i18n translations to the DOM.
 */
function applyTranslations() {
  const lang = getCurrentLang();
  const dict = _getDict();

  // Text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) el.textContent = dict[key];
  });

  // innerHTML (for keys with HTML markup)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });

  // Placeholder attributes
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (dict[key] !== undefined) el.placeholder = dict[key];
  });

  // Title attributes (tooltips)
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    if (dict[key] !== undefined) el.title = dict[key];
  });

  // Aria-label attributes
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const key = el.getAttribute('data-i18n-aria');
    if (dict[key] !== undefined) el.setAttribute('aria-label', dict[key]);
  });

  // Document direction and lang attr
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

  // Mark active lang button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    const isActive = btn.dataset.lang === lang;
    btn.classList.toggle('lang-btn--active', isActive);
  });

  // Sync language select dropdowns
  document.querySelectorAll('.lang-select').forEach(sel => {
    sel.value = lang;
  });
}

/**
 * Switch language and persist it.
 */
function setLang(lang) {
  if (!translations[lang]) return;
  localStorage.setItem('lang', lang);
  _cachedLang = null; // invalidate memoized dict so _getDict() rebuilds on next call
  applyTranslations();
}

/**
 * Auto-initialize on DOM ready.
 */
(function initI18n() {
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', applyTranslations);
  } else {
    applyTranslations();
  }
})();
